jQuery(document).ready(function () {
    var iRegion = 0;
    var id_region = 0;
    var precio_despacho = 0;
    var htmlRegion = '<option value="sin-region">-- Seleccionar región --</option>';
    var htmlComunas = '<option value="sin-comuna">-- Seleccionar comuna--</option>';

    jQuery.each(RegionesYcomunas.regiones, function () {
        htmlRegion = htmlRegion + '<option value="' + RegionesYcomunas.regiones[iRegion].NombreRegion + '">' + RegionesYcomunas.regiones[iRegion].NombreRegion + '</option>';
        iRegion++;
    });

    jQuery('#regiones').html(htmlRegion);
    jQuery('#comunas').html(htmlComunas);

    jQuery('#regiones').change(function () {
        var iRegiones = 0;

        var valorRegion = jQuery(this).val();
        var htmlComuna = '<option value="sin-comuna">-- Seleccionar comuna--</option>';
        jQuery.each(RegionesYcomunas.regiones, function () {
            if (RegionesYcomunas.regiones[iRegiones].NombreRegion == valorRegion) {
                var iComunas = 0;

                id_region = RegionesYcomunas.regiones[iRegiones].id_region;
                precio_despacho = RegionesYcomunas.regiones[iRegiones].precio_despacho_R;
                jQuery.each(RegionesYcomunas.regiones[iRegiones].comunas, function () {
                    htmlComuna = htmlComuna + '<option value="' + RegionesYcomunas.regiones[iRegiones].comunas[iComunas] + '">' + RegionesYcomunas.regiones[iRegiones].comunas[iComunas] + '</option>';
                    iComunas++;
                });
            }
            iRegiones++;
        });
        jQuery('#comunas').html(htmlComuna);
    });
    jQuery('#comunas').change(function () {
        if (jQuery(this).val() == 'sin-comuna') {
            alert('seleccione Región');
        } else if (jQuery(this).val() !== 'sin-comuna') {
            $('#RegionSlected').text($('#regiones').val());
            $('#ComunaSlected').text($('#comunas').val());
            $('#pre_despacho').text(precio_despacho);

        }
    });
    jQuery('#regiones').change(function () {
        if (jQuery(this).val() == 'sin-comuna') {
            alert('seleccione Región');
        }
    });


})



$(document).ready(function () {



    if ($('#radio').length > 0) {

        $('[name="color_group"]').on('change', function () {
            if (this.checked) {
                $("#cantidad").val(1);
                $("#stock").val($(this).attr('data-stock'))

                $("#cantidad").on("change", function () {
                    cantidad = parseInt($("#cantidad").val());
                    stock = parseInt($("input:radio[name=color_group]:checked").attr('data-stock'));

                    console.log($("#stock").val());
                    if (cantidad <= 0) {
                        $("#cantidad").val("1");
                    }

                    if (cantidad > stock) {
                        $("#cantidad").val(stock);
                    }
                });
            }
        })
    } else {
        document.getElementById('stockdisponible').innerHTML = 'SIN STOCK'
        $('#carrito').attr('disabled', true);
    }

    $("#carrito").on("click", function () {
        var url = $("#urlCarrito").val();
        var urlRed = $("#urlRedireccion").val();
        var sku = $("#id_producto").val();
        var cant = $("#cantidad").val();
        var Cod_Tienda = $("#Cod_Tienda").val();
        var col = $("input:radio[name=color_group]:checked").val();
        var monto = $("#precio").val();
        var descripcion = $("#descripcion").val();
        $.ajax({
            url: url,
            type: "GET",
            data: {
                _token: $("meta[name='csrf-token']").attr("content"),
                sku: sku,
                Cod_Tienda: Cod_Tienda,
                cant: cant,
                col: col,
                monto: monto,
                descripcion: descripcion
            },
            success: function (data) {
                if (data["status"] == 0) {
                    window.location.replace(urlRed);
                } else {
                    toastr["warning"](
                        "Ha ocurrido un problema, por favor vuelva a intentarlo"
                    );
                    console.log(data);
                }
            }
        });
    });
});
